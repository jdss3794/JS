const calculator = {
  add: function (a, b) {
    console.log(a + b);
  },
  min: function (a, b) {
    console.log(a - b);
  },
  div: function (a, b) {
    console.log(a / b);
  },
  mul: function (a, b) {
    console.log(a * b);
  },
  powerof: function (a, b) {
    console.log(a ** b);
  },
};

calculator.add(2, 4);
calculator.min(2, 4);
calculator.div(2, 4);
calculator.mul(2, 4);
calculator.powerof(2, 4);
